<h2>2º DIA - ÁGUAS CALIENTES - MACHU PICCHU - CUSCO</h2>
No dia seguinte bem cedo sairemos rumo a Cidadela Perdida de Machu Picchu, onde desfrutaremos o amanhecer neste fantástico lugar antes da chegada das pessoas que vem de trem. <br>
<br>
Aproveitaremos nosso tempo para receber todas as informações sobre Machu Picchu, com nosso guia (Aprox. 2 ½ horas) pelo Templo do sol, Templo das 3 janelas, Relógio solar, Templo do Cóndor e pelo Jardim botânico e muito mais, Depois terão um tempo livre para caminhar pelos arredores de &quot;Machu Picchu&quot; ou subir em Wayna Picchu, onde se pode observar toda a cidadela de Machu Picchu, com seus vales e montanhas que rodeiam toda a cidadela perdida . No final da tarde retornaremos ao povoado de Águas Calientes e poderão visitar as Águas Termais enquanto esperaremos o trem para Ollantaytambo. Logo após  nos pegaremos o transporte para Cusco.<br>
