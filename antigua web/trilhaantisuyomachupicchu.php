      <h2>1º DIA - CUSCO - LAGUNA QORI COCHA - OLLANTAYTAMBO - ÁGUAS CALIENTES </h2>
Pela manhã buscaremos no hotel as 07h00 e  em 20 minutos chegaremos ao Parque Arqueológico de Sacsayhuaman Chincana Grande (caverna grande), onde iniciaremos nossa caminhada de 4 horas a ritmo moderado. Passando por Nusta pakana, Balcão do diabo, mirador del cóndor y Pucamocco (4.100 m.a.n.m). Até chegar ao Lago de Qori cocha (lago do ouro) e almoçaremos na borda do lago. <br>
<br>
No trajeto apreciaremos as 5 montanhas mais altas da região de Cusco, <strong>APU SALKANTAY, VERONICA , CHICON, PITUSIRAY, Y AUSANGATE</strong> simplesmente maravilhoso. <br>
<br>
À tarde desceremos aproximadamente por 2 horas apreciando todas paisagens da região. Visitaremos o Templo Sixtina de Humasbamba a ( 3,700 m.a.n.m). Chegaremos ao povoado Chinchero, onde tomaremos um translado para Ollantaytambo por 1h00 hora. Após pegaremos o trem até o povoado de Águas Calientes e pernoitamos no hotel. <br>
