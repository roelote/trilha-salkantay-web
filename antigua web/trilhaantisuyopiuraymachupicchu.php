      <h2>1º DIA - CUSCO - LAGUNA QORI COCHA - CAMPING PIURAY . </h2>
Pela manhã buscaremos no hotel, em 20 minutos chegaremos ao Parque Arqueológico de Sacsayhuaman Chincana Grande (caverna grande), onde iniciaremos nossa caminhada de 4 horas a ritmo moderado. Passando por Nusta pakana, Balcão do diabo, mirador del cóndor y Pucamocco (4.100  m .a.n.m). Até chegar ao Lago de Qori cocha (lago do ouro) e almoçaremos na borda do lago. <br>
<br>
No trajeto apreciaremos as 5 montanhas mais altas da região de Cusco, <strong>APU SALKANTAY, VERONICA , CHICON, PITUSIRAY, Y USANGATE</strong> simplesmente maravilhoso. <br>
<br>
À tarde desceremos aproximadamente por 2 horas apreciando todo a paisagens da região. Visitaremos o Templo Sixtina de Humasbamba e chegaremos a nosso Acampamento Piuray (3.600 m.a.n.m), onde estarão nos esperando com um chá de Muña para nosso pernoite. <br>
<div class="image"><img src="images/tours/antisuyo5.jpg" alt="TRILHA ANTISUYO A MACHU PICCHU"><img src="images/tours/antisuyo6.jpg" alt="TRILHA ANTISUYO A MACHU PICCHU"></div>
<br>
<h2>2º  DIA:  CAMPING PIURAY -   ÁGUAS CALIENTES</h2>
Depois do café da manhã típico e ouviremos explicações do Lago Sagrado em sua Margem. Visitaremos o Palácio do Inca Tupaq Yupanqui com instruções peculiares de Sacsayhuaman, onde se fundou o povoado Chinchero e a bandeira de Cusco chamadaTAWANTINSUYO (quatro regiões).<br>
<br>
A visita mais importante é o Tunel de conexão de Chincana Chico com o Templo do Sol em Cusco. Aqui desceremos 7 km pelo caminho Inca até chegar ao Povoado de Urquillos (um povoado Quechua, com casas e fazendas que foram feitas sobre  construções Incas). Veremos fabulosa variedade de milho que tem um dos maiores grão do mundo chamado PARAQAY. <br>
<br>
Falaremos com as famílias e comemoram com a Chica (cerveja Inca) chamado Aqha, que significa Força. E desfrutaremos de um delicioso almoço campestre e típico da região. Logo tomaremos um ônibus de 30 minutos até a estação de ollantaytambo, onde tomaremos nosso trem até a cidade de Águas Calientes e pernoite no hotel em Águas Calientes. <br>
<div class="image"><img src="images/tours/antisuyo7.jpg" alt="TRILHA ANTISUYO A MACHU PICCHU"><img src="images/tours/antisuyo8.jpg" alt="TRILHA ANTISUYO A MACHU PICCHU"></div>
<br>
<h2>3º DIA - MACHU PICCHU - CUSCO</h2>
No dia seguinte bem cedo sairemos rumo a Cidadela Perdida de Machu Picchu, onde desfrutaremos o amanhecer neste fantástico lugar antes da chegada das pessoas que vem de trem. <br>
<br>
Aproveitaremos nosso tempo para receber todas as informações sobre Machu Picchu, com nosso guia (Aprox. 2 ½ horas) pelo Templo do sol, Templo das 3 janelas, Relógio solar, Templo do Cóndor e pelo Jardim botânico e muito mais, Depois terão um tempo livre para caminhar pelos arredores de &quot;Machu Picchu&quot; ou subir em Wayna Picchu, onde se pode observar toda a cidadela de Machu Picchu, com seus vales e montanhas que rodeiam toda a cidadela perdida . No final da tarde retornaremos ao povoado de Águas Calientes e poderão visitar as Águas Termais enquanto esperaremos o trem para Ollantaytambo. Logo após  nos pegaremos o transporte para Cusco. <br>
