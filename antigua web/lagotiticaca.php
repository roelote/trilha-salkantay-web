    <h2 class="font-h2">ILHA TAQUILE</h2>
    Estendem-se aproximadamente seis quilômetros, variando apenas sua altitude entre o porto e o povo, de 3800 m.a.n..m a 3900. Sua temperatura máxima oscila os 23ºC e a mínima os 7ºC.<br>
    Os vestígios da ilha datam de épocas pré-incas. Durante a colônia e até os primeiros anos do século XX, o lugar foi utilizado como prisão política, mas a partir de 1970 a ilha passou a ser propriedade exclusiva dos taquilenhos.<br>
    Taquile caracteriza-se por seus amigáveis moradores, quem conservam seus costumes, tradições e vestimentas como antigamente.<br>
    <br>
    Os moradores se destacam pelos seus laboriosos e finos têxteis com decorações simétricas simbólicas, de cores fortes, que refletem seu estilo de vida, seus costumes e suas crenças andinas.<br>
