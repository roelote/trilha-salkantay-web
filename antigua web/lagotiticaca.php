  <h2 class="font-h2">O LAGO TITICACA</h2>
    É o maior lago navegável comercialmente do planeta com uma altitude 3.810 m.a.n.m. consta com 8.3100 km2.<br>
    Quarenta e uma ilhas povoadas, fazem parte desse gigantesco e mitológico lago andino, que provêm das águas do desgelo das montanhas ao seu redor, uma profundidade de  300 metros a máxima e a mínima de 160 metros.<br>
    O Lago Titicaca foi de grande importância para os Incas. Diz à lenda que foi das águas do Lago Titicaca que emergiu o primeiro Inca Manco Capac  junto a sua mulher Mama Ocllo, por ordem do Deus Sol, com a função de fundar o Império inca na cidade de Cusco<br>
    No período de outubro e maio, a temperatura pode variar entre os 9° C e os 11° C, enquanto que de junho a setembro varia entre os -7° C e -10° C.<br>
    A fauna está composta principalmente de patos, zambullidores, chulumpi tiki, guallatas (gaivotas andinas) e parihuanas ou flamengos.  Entre os peixes destacam o suche (Trichomycterus rivulatus), o carachi e a truta. No lago existe uma espécie conhecida como rã gigante do lago Titicaca (Telmatobius culeus), sendo o lago único hábitat de esta espécie.<br>
