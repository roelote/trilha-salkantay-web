    <br>
    <h2 class="font-h2">BALCÃO DO CONDE DE LEMOS</h2>
    Construída desde a fundação da cidade, dizem que nesta casa se hospedou o virei Conde de Lemos quando chegou à zona para sufocar uma revolta.<br>
    Atualmente funciona no lugar o complexo cultural do Instituto Nacional de Cultura da região Puno e conta com uma importante galeria de arte.<br>
    <div class="image"><img src="images/tours/punobalcaocondelemos.jpg" alt="BALCÃO DO CONDE DE LEMOS"><img src="images/tours/punobalcaocondelemos1.jpg" alt="BALCÃO DO CONDE DE LEMOS"></div>
    <br>
    <h2 class="font-h2">MALECÃO BAHIA DOS INCAS</h2>
    O malecão da Bahia dos Incas é um passeio para pedestres com uma charmosa vista do lago, onde se encontram as sukankas ou intihuatanas, cuja função principal era a de servir como relógios solares.<br>
    <br>
    As culturas pré-incas as utilizavam, além disso, para assinalar os lugares onde se realizavam os atos cerimoniais e de sacrifício, servindo também para delimitar as terras entre as comunidades. 
