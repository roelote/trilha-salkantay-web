    <h2 class="font-h2">ILHA FLUTUANTE DE UROS</h2>
    A atração principal do Lago Titicaca são as ilhas flutuantes, um povo exótico do período pré-Incaico que construíram suas ilhas de totora , planta aquática com hastes grossas parecida com cana-de-açúcar oca,  amarrada aos feixes com cordas e madeira, sobrepostas em camadas  formando a base das ilhas, e sobre a ilha são construídas as casas, ao passar do tempo, apodrece a camada embaixo da agua,que é substituída por outra camada na superfície, e assim perpetua a ilha, é muito resistente e surpreendente a construção das ilhas dos Uros , as casas tem todo o aparato para viver uma família, até um fogão de pedra que serve para cozinhar e como refugio das águas frias do Lago Titicaca.<br>
    A raiz da totora também é usada como alimento, os talos servem como alimento, as folhas e flores como remédios.<br>
