    <br>
    <h2 class="font-h2">ILHA AMANTANÍ</h2>
    A ilha Amantaní tem uma extensão de aproximadamente nove quilômetros quadrados. A flora do lugar se caracteriza pela presença de plantas arbustivas como a muña, kantuta, salvia, tola e patamuña.<br>
    <br>
    Na ilha moram oito comunidades que se dedicam especialmente ao cultivo de batata, milho, oca, quinua, favas e ervilhas e seu artesanato mais representativo está constituído pelos têxteis e o talhado em pedra.<br>
    <br>
    Entre seus atrativos naturais destacam-se dois mirantes na parte mais alta, de onde se pode apreciar o lago em toda sua extensão, bem como alguns restos pré-hispânicos, centros cerimoniais e um cemitério de múmias.<br>
    <div class="image"><img src="images/tours/punoilhaamantani.jpg" alt="ILHA AMANTANÍ"><img src="images/tours/punoilhaamantani1.jpg" alt="ILHA AMANTANÍ"></div>
    <br>
    <h2 class="font-h2">CHULLPAS DE SILLUSTANI</h2>
    Trata-se da zona arqueológica localizada exatamente a 34 quilômetros da capital, catalogada como uma das necrópoles mais importante do mundo. Define-se como uma grande explanada rodeada pela lagoa de Umayo.<br>
    <br>
    Ali se pode apreciar um cemitério de impressionantes tumbas pertencentes à cultura Kolla (1200-1450) desenvolvida na parte norte da lagoa na localidade conhecida como Hatuncolla.<br>
    <br>
    A forma particular dos túmulos, são troncos de cone invertidos, chamados chullpas, são construções que se localizam embora em um número menor, em outros vários locais do altiplano, como Acora ou Ilave.<br>
