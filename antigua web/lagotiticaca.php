    <br>
    <h2 class="font-h2">A CATEDRAL</h2>
    Sua construção acabou se no século XVIII, destaca-se pela qualidade de sua estrutura e seus talhados em pedra, pois seu frontispício foi esculpido pelo escultor peruano Simón de Asto.<br>
    <br>
    Esta igreja é uma mostra do barroco espanhol e inclui elementos andinos que conferem ao monumento seu caráter eminentemente mestiço.<br>
    <div class="image"><img src="images/tours/punocatedral.jpg" alt="A CATEDRAL"><img src="images/tours/punocatedral1.jpg" alt="A CATEDRAL"></div>
    <br>
    <h2 class="font-h2">CERRO DE HUAJSAPATA</h2>
    Huajsapata, que significa “testemunha de meus amores”, é um mirante natural de onde se pode contemplar a cidade e o lago Titicaca. No seu topo localiza-se um monumento a Manco Cápac, fundador do Império Inca, dizem que no cerro existem umas cavernas por onde discorrem caminhos subterrâneos que comunicam Puno com o templo de Koricancha na cidade do Cuzco.<br>
