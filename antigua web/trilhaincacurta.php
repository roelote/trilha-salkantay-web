<h2>DIA 2: ÀGUAS CALIENTES - MACHU PICCHU  - CUSCO </h2>
Muito cedo sairemos caminhando ou  pegaremos o bus até Machupicchu, para poder observar o amanhecer e nascer do  sol neste mágico lugar, antes dos outros turistas chegarem ao machupicchu,  claro que será para aquelas pessoas que desejam subir e desfrutar um pouco mais  de Machupicchu,<br>
<br>
Desfrutaremos a tranquilidade da cidadela  perdida dos Incas em um tour com nosso guia (Aprox. 2 ½ horas) pelo Templo do  sol, Templo das 3 janelas, o relógio solar, Templo do Cóndor e pelo Jardim  botânico e muito mais, Depois terão um tempo livre para caminhar pelos  arredores de &quot;Machupicchu&quot; ou subir em Huaynapicchu, onde se pode  observar toda cidadela de Machu Picchu, com vales e montanhas que rodeiam toda  a cidade e também visitar o Templo da Lua e a famosa Ponte do Inca.<br>
<br>
A tarde desceremos até o povoado de Águas  Calientes onde tomaremos o trem de retorno até Poroy ou Ollantaytambo e de lá  terá um bus que estará nos transladando até Cusco.&nbsp;<br>
