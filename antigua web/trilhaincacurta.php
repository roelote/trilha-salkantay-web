      <h2> DIA 1: CUSCO - WIÑAYWAYNA - ÀGUAS  CALIENTES</h2>
Estaremos passando no hotel aprox. às  6:00a.m., e vamos até a estação de trem de Poroy ou Ollantaytambo, de acordo  com as disponibilidades de espaço no trem, que nos transportará até o Km. 104  onde damos inicio a nossa caminhada em uma paisagem de selva subtropical,  visitaremos o grupo arqueológico de Chachabamba (2150 m.a.n.m) e em  aproximadamente 3 horas de caminhada chegaremos a Wiñaywayna complexo  impressionante composto de um centro agrícola com numerosos terraços, um setor  religioso e um setor urbano, logo subiremos até Intipunku, &ldquo;Porta do Sol&rdquo;,  durante o trajeto de 1 hora de caminhada por uma estrada de pedras planas e uma  vista panorâmica das montanhas, ponto onde poderemos desfrutar a primeira vista  do majestoso &quot;Machupicchu&quot; e Inti Punku, logo desceremos até  &quot;Machupicchu&quot; para iremos ate Águas Calientes para pernoitar.<br>
<br>
Duração da caminhada é de 6 a 7 horas  aproximadamente, a acomodação è durante a noite em hospedagem previamente  selecionada, aproveitando o restante do dia para desfrutar das Águas Termais no  povoado de Águas Calientes.<br>
