A<strong> trilha Inca </strong>de 2 dias é uma versão curta do famosa <strong>trilha inca</strong> de 4 dias, além do mais é  uma ótima opção para aquelas pessoas que não dispõem de muito tempo e também  para aqueles cuja condição física e idade não ajudam em um trekking mais longo  e não querem perder está experiência única.<br>
<br>
Para esta viagem usaremos o trem como transporte  que é a única opção até o KM 104 e caminharemos até o complexo arqueológico de  Wiñayhuayna, e logo nos dirigiremos até Inti Punku (chamada de porta do sol) a  2,650 m.a.n.m, onde podemos ver um pôr do sol deslumbrante e logo seguiremos até  o povoado de Águas Calientes, onde passaremos a noite em um hotel.<br>
No dia seguinte bem cedo sairemos rumo a  Cidadela Perdida de Machu Picchu, onde desfrutaremos o amanhecer neste  fantástico lugar antes da chegada das pessoas que vem de trem.&nbsp;<br>
Aproveitaremos nosso tempo para receber todas as  informações sobre Machu Picchu, com nosso guia (Aprox. 2 ½ horas) pelo Templo  do sol, Templo das 3 janelas, Relógio solar, Templo do Cóndor e pelo Jardim  botânico e muito mais, Depois terão um tempo livre para caminhar pelos  arredores de &quot;Machu Picchu&quot; ou subir em Wayna Picchu, onde se pode  observar toda a cidadela de Machu Picchu, com seus vales e montanhas que  rodeiam toda a cidadela perdida .<br>
