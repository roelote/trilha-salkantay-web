<h2>1º DIA - CUSCO - LAGUNA QORI COCHA - CHINCHERO - CUSCO</h2>
Pela manhã buscaremos no hotel as 07h00 e em 20 minutos chegaremos ao Parque Arqueológico de Sacsayhuaman Chincana Grande (Caverna grande), onde iniciaremos nossa caminhada de 4 horas a ritmo moderado. Passando por Nusta pakana, Balcão do diabo, mirador del cóndor y Pucamocco (4.100  m.a.n.m). Até chegar ao Lago de Qori cocha (lago de ouro) e almoçaremos na margem do lago. <br>
No trajeto apreciaremos as 5 montanhas mais altas da região de Cusco, <strong>APU SALKANTAY, VERONICA , CHICON, PITUSIRAY, Y AUSANGATE</strong> simplesmente maravilhoso. <br>
<br>
