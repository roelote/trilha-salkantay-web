        El Vale Sagrado dos Incas, dos Andes peruanos, está composto por numerosos rios que descendem por quebradas y pequenos vales; pensei numerosos monumentos arqueológicos y povos indígenas.<br>
        Este vale foi muito apreciado pelos incas devido a suas especiais qualidades geográficas e climáticas. Foi uno dos principais pontos de produção pela riqueza de suas terras e o lugar donde se produze o melhor grão de milho no Perú.<br>
  <br>
        A primeira visita do dia será no mercado artesanal de <strong>QORAO</strong> e logo depois ao mercado de <strong>PISAQ</strong> onde encontraremos produtos artesanais, como: cerâmica, prataria, bijuterias, roupas, tecidos, mantas e todo tipo de trabalhos feitos pela população da região, é a melhor alternativa para fazer compras, porque os preços não estão sobre feito de impostos e ficam más econômicas que na cidade.<br>
  <br>
        Também temos a singular feira que se realiza nos domingos na Plaza de Armas de Pisaq , sua importância começa seu intercambio comercial pré-hispanico denominado<strong> Rantiy ou trueque</strong>, que não necessitava de moeda, os protagonistas são as pessoas da região que intercambiam seus produtos por outros.<br>
  <br>
        Depois das compras, continuamos com nosso passeio ao Centro Arqueológico de Pisaq, onde o guia lhes dará explicações de todos os centros importantes como: o Relógio Solar (Intiwatana), Centro Agrícola, sítios urbanos, Centros de Vigilância, Centro Astronômico e o Canal de Água, depois desceram para o povoado de Pisaq para continuar o passeio e passaremos pelos povoados de Lamay, Calca, Yucay que são povoados indígenas que completam o Vale Sagrado, Urubamba será o lugar que irão almoçar.<br>
  <br>
        Depois do almoço continuamos com nossa viagem para o Parque Arqueológico de <strong>OLLANTAYTAMBO</strong>, foi uma construção militar levantada para proteger a Capital do Império Incaico das possíveis invasões ao Norte. Também se diz que foi construída para habilitar os caminhos que  iriam ao Antisuyo. O que não se discute é que foi uma cidade muito fortificada, rodeada de uma parede como uma fortaleza. A principal entre elas é chamada Casa Real do Sol, e também podemos encontrar as fortalezas de Choqana e Inkapintay no lado esquerdo do Rio Urubamba, e também encontramos o Tunupa, que é considerado como ó Deus  Wiracocha e descrito como um personagem dotado de poderes sobrenaturais.<br>
  <br>
        O complexo arqueológico de Ollantaytambo foi um  base militar, religioso, administrativa e agrícola. O cenário arquitetônico e impressionante pelo seu tamanho, estilo e originalidade  de seus edifícios.<br>
  <br>
