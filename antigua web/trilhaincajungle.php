<br>
<br>
<h2>DIA 02: SANTA MARIA - SANTA TERESA (TREKKING)</h2>
Depois de um bom café da manhã, com  muitas vitaminas a base de banana, laranjas, mamão e outras frutas da região,  estaremos dispostos a começar a nossa fantástica caminhada cheia de aventuras,  caminharemos por lugares cheios de vegetação onde podemos apreciar o cultivo e  a secagem do café , assim como a planta sagrada dos Incas a folha de COCA,  caminharemos por segmentos de caminhos incas originais, onde encontraremos com  a história&nbsp;<strong>kapac ñan</strong>&nbsp;(redes de caninhos inca), hoje degradada pelo tempo, depois da  energética caminhada com seu ar mais puro do mundo, teremos a oportunidade de  ingressar aos Banhos Termos-Medicinais de Cocalmayo em Santa Teresa para  relaxar depois de um dia de caminhada, chegaremos a Santa Teresa onde  jantaremos e dormiremos, na hora do jantar teremos uma coordenação do guia para  o dia seguinte.<br>
<div class="image"><img src="images/tours/santateresa.jpg" alt="TRILHA INCA JUNGLE"><img src="images/tours/santateresa1.jpg" alt="TRILHA INCA JUNGLE"></div>
<br>
<h2>DIA 03: SANTA TERESA - ÁGUAS CALIENTES (CAMINHADA)</h2>
Este dia é memorável por sua  variedade de flora e fauna que veremos no decorrer do dia, como borboletas,  loros, iguanas, aves e insetos.<br>
Este dia sairemos às 9h45 em busca das pontes  levadiças assim como cruzar o Rio Vilcanota (Rio Sagrado dos Incas) através de  uma tirolesa onde poremos a prova nossa aventura e adrenalina, este ponto é um  dos mais emocionantes e divertidos. e também visitaremos o Inti Watana (relógio  solar) e almoçaremos na Hidroelétrica com comidas típicas da zona.<br>
<br>
Finalmente chegaremos a Águas Calientes, a  última noite da viagem, teremos um jantar e uma reunião com nossos guias para  indicar sobre o dia mais grandioso de suas vida, a Visita a Cidade Sagrada de  Machu Picchu.<br>
<div class="image"><img src="images/tours/aguascalientes.jpg" alt="aguas calientes"><img src="images/tours/aguascalientes1.jpg" alt="machu picchu pueblo"></div>
<br>
<h2>DIA 04: MACHU PICCHU FULL DAY</h2>
Depois do café da manhã, começaremos  a caminhada rumo ao sítio arqueológico de Machu Picchu uma das Maravilhas do  Mundo Moderno, para apreciar o amanhecer de Machu Picchu, nesta caminhada nos  subiremos desde 1980 m.a.n.m até a chegada em 2450 m.a.n.m. A visita guiada por  um Guia Oficial em Inglês ou Espanhol terá duas horas de duração, e em seguida,  terão tempo livre para tirar fotos, e caminhar por sua conta, meditar, e entre  outros, neste mesmo dia você estará retornando ao Cusco, tomaremos o trem de  Águas Calientes a Ollantaytambo, onde nosso van ou bus estará aguardando para  levá-los até Cusco chegando a as 21h45.<br>
