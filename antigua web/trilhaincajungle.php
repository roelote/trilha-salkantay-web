      <h2>DIA 01: CUSCO - SANTA MARIA (BIKE)</h2>
Primeiramente nosso transfer passará  para buscá-los no hotel meia hora antes da saída, nossa viagem dura por volta  de 4h30m e no decorrer da viagem observaremos impressionantes paisagens, como  as montanhas mais altas da região, como o Nevado Veronica e Pitusiray, que se  encontra quase a 5800 m.a.n.m, também vamos ver os pitorescos povoados de  Poroy, chinchero e Ollantaytambo onde tomaremos a pista pela margem direita  para elevarmos até a altura de 4350 metros sobre o nivel do mar, encontraremos  a Abra del Malaga, ponto místico onde se realiza cerimônias de agradecimento a  Pachamama (Terra Mãe) com muita freqüência. Neste ponto já poderemos observar  todo o Vale da Convencion, onde também será o ponto onde podemos coordenar com  o guia para começar nossa aventura Full Adrenalina, mas no caso que esteja  neblina baixa, desceremos com o carro até um local que se chama San Luis onde  nosso guia nos entregará as bicicletas, onde começaremos uma das excursões mais  lindas do mundo comparada somente com a de Coroico na Bolivia, iremos descendo  em Down Hill, observando a Imensidão do Vale da Convencion e conforme  desceremos irão encontrar diferentes espécies da flora e fauna, visitaremos na  metade do caminho as Ruínas dos Inkas do Wamanmarka.<br>
<br>
Iremos almoçar e seguiremos viagem por mais 4  horas de bike e chegaremos a 1430 metros de altura em Santa Maria. (nossa  primeira noite em um lodge de serviços básicos). Onde teremos o jantar em uma  região de floresta e teremos um pequeno resumo e comentário sobre o trekking do  dia seguinte com todo o grupo.<br>
