<strong>CUSCO.-</strong> Segundo a lenda, Cuzco ou Cusco era uma cidade sagrada e a capital do Império Tahuantinsuyo, fundada por volta dos séculos XI e XII d.C. pelo Inca Manco Ccapac, nascido no sagrado Lago Titicaca. Cusco foi o centro do governo das quatro grandes regiões do Império Inca, que veio  abranger uma grande parte do que é comumente chamado nos dias de hoje como; Equador, Colômbia, Peru, Bolívia, Argentina e Chile.<br>
<br>
<a href="trilhainca.php" title="Trilha Inca">TRILHA INCA </a> <strong>MACHU PICCHU:</strong> Machu Picchu ou Machu Pikchu (em quéchua - língua falada pelos incas), que significa “Montanha Velha”, é o nome atual de uma llaqta (antigo povoado andino inca) de pedra, construído por volta do século XV no cume de uma montanha, se conecta às montanhas Huayna Picchu ao leste e aos Andes Centrais ao sul do Peru. Seu nome original seria Picchu ou Picho.<br>
