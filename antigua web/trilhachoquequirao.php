<strong>Coquequirao</strong> ou Berço de Ouro em quéchua fica em numa quebrada a 3.085 metros de altitude, esculpida em pedra pelos Incas, onde a organização do Império Tawantinsuyo (Quatro regiões) era estritamente dirigida por um governante Inca. <strong>Choquequirao</strong> esta protegida pelo rio Apurímac. e esta rodeada pelos nevados de Yanama, Ampay, Choquetarpu e Pumasillo .Trata-se de uma clássica cidade inca, com setores sagrado, templos ritualísticos, centros astronômicos, urbanístico e agrícola. Apenas 30% de sua área total se encontram escavados, mas uma parceria entre os governos do Peru e da França promete acelerar os trabalhos e, eles apostam transformá-la numa nova <strong>Machu Picchu</strong>.<br>
<br>
<strong>Choquequirao</strong> foi o ultimo bastião da resistência inca ante a invasão espanhola, e faz parte da trilha mais desafiante da Cordilheira dos Andes. Limitada pelos profundos cânions do Apurímac e  Willcamayu, <br>
Conhecer de perto tanto Vilcabamba como <strong>Choquequirao</strong> exige tempo e ainda mais espírito de aventura do que percorrer a <strong>Trilha de Salcantay </strong>ou mesmo a <strong>Trilha Inca</strong>.<br>
