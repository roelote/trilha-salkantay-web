      <h2>DIA 01: CUSCO - CACHORA - COCAMASANA - PLAYA ROSALINA</h2>
Saímos às 4h:00 da cidade de Cusco nos dirigindo a Cachora. Chegando aproximadamente as 7h30 no povoado de Cachora, onde tomaremos o café da manhã e preparemos nossos equipamentos nos cavalos para logo começar nossa caminhada. <br>
