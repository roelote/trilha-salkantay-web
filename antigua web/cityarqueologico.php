<h2>INTI RAYMI.</h2>
Como sugestão aos amantes do misticismo e do esoterismo, o complexo de <strong>SACSAYHUAMAN</strong> é um excelente local para a captação de energia e meditação. <br>
<br>
A continuação teremos a visita a alguns dos centros arqueológicos mais importantes de Cuzco, chamado de <strong>QENQO</strong> em quéchua, “<strong>Labirinto</strong>” conhecido como a representação dos três mundos, como: Hanac Pacha (Mundo de arriba representado pelos Deuses), Kay Pacha (Mundo real representado pelos Vivos), Ujhu Pacha (mundo de abaixo representado pelos mortos), Na parte central do pátio veremos um monólito gigante que e uma <strong>Waka</strong> em quéchua quere dizer (lugar Sagrado) com representação de um gigantesco felino o Puma, que foi um centro adora tório, onde se observa as 19 hornacinas (janelas retangulares) que serviam para colocar as oferendas ao <strong>Malkis</strong> que eram as múmias com o fim de elas também participar do ritual.<br>
<br>
Depois prosseguimos com nosso tour, para visitar o Centro Arqueológico de <strong>PUCAPUKARA</strong> que foi um dos centros de controle importante para o ingresso de pessoas e da Real Nobreza a capital do Tahuantinsuyo que o Cusco, foi também um dos centros que servia como quartel militar e lugar de descanso.<br>
<br>
Terminamos nosso tour com a visita a <strong>TAMBOMACHAY</strong> em quéchua (tanpu mach'ay, lugar de descanso) está localizado próximo a Cusco. É um sítio arqueológico que foi destinado ao culto à água e para que o chefe do Império Inca pudesse descansar. Este lugar também é denominado &quot;Banhos do Inca”. É composto de uma série de aquedutos, canal e várias cascatas de água que correm pelas rochas.<br>
<br>
Aqui também houve uma espécie de jardim real, cuja irrigação provinha de um complicado sistema de canais especialmente feitos para essa função. <br>
<br>
Hoje em dia as gerações atuais mantêm a costume de tomar a água para fertilidade e para o rejuvenescimento do estado físico.<br>
