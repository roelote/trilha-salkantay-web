<h2>SALINERAS:</h2>
Ou &quot;Mina de Sal”, localizada a noroeste do povoado de Maras,este lugar esta constituídos por cerca de 3.000 poços pequeno, com uma área média de 5 metros quadrados durante a estação seca são preenchidos ou regados a cada três dias com água salgada que vem de um afluente natural situado no topo dos poços quando a água se evapora se, a sal fica e se solidifica gradualmente. Então a sal é bem moída e granulada, em seguida, ensacado em sacos plásticos e enviados para os mercados a região, hoje em dia a sal e iodado para seu consumo que não é prejudicial para a saúde.<br>
