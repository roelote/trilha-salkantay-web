<h2>MARAS:</h2>
Localizado ao oeste de Cusco a 3300 metros. Acima do mar. Onde poderão apreciar a cordilheira de Urubamba e os picos nevados de &quot;Verônica&quot; a (5682 m.a.n.m) e &quot;Chikón&quot; (5530 m.a.n.m), A ocupação do lugar começou quando os nobres da realeza inca foram despojados e expulsados de seus palácios que tinham em Qosqo (cusco) e eles tiveram que estabelecer em outras cidades pequenas como Maras. Tem uma igreja colonial feita de adobe (tijolos de barro), típica arquitetura dos povoados da serra, o interior da igreja alberga pinturas da Escola cusquenha.<br>
