<h2>MORAY:</h2>
Situa-se a 7 km ao sudoeste de Maras. Moray e um centro arqueológico e único em seu gênero, são gigantescas depressões naturais ou buracos na superfície do solo, que foram usados para construir terraços agrícolas em contorno ou terraços tem seus canais de irrigação, é, portanto e um protótipo de invernadeiro ou estação biológica experimental bastante avançada para a época que ajudou a homem sul americano para poder domesticar as plantas. Os povoadores da zona herdado estas artes hoje em dia  60% dos produtos vegetais são produzidos em esta zona para o consumo do homem andino <br>
Em na atualidade no peru se conhece um milhar e meio de variedades de batatas, cento e cinqüenta de milho, e muitos outros produtos.<br>
