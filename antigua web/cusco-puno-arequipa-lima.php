<p><strong>Pacotes Peru: Cusco - Puno - Arequipa - Lima 12 dias 11 noites</strong> é um pacote idial para você conecher aos principais pontos turisticos do país, começando pela capital, <strong>Lima</strong>, visitando aos principais sitios turiticos, como: a<strong> City Tour pela cidade colonial e moderna</strong>, o complexo arqueológico de <strong>Huaca Pucllana</strong>, circuito magico das aguas e <strong>gastronomia peruana</strong>.</p>
<p>Após continuamos a <strong>cidade de Cuzco</strong>, conhecido "<strong>o umbigo do mundo e o império dos Incas</strong>" visitar aos principais <strong>sítios arqueológicos</strong>, como: <strong>City tours arqueológico</strong>, ao <strong>Vale Sagrado dos incas</strong> e opção de pernoitar a noite no povoado de <strong>Aguas Calientes</strong> ao lado de uma das <strong>maravilhas do mundo moderno</strong>: <strong>Machu Picchu</strong>.</p>
