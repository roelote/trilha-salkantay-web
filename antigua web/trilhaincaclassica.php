    <div id="tab1" class="tab_content"><strong>Dia 01: TRILHA INCA</strong>
      <h2>CUSCO - KM 82 - HUAYLLABAMBA </h2>
Buscaremos vocês no Hotel às 6:00 com nosso bus e nos  dirigiremos a Piscakucho (2700) Povoado conhecida como o Km 82 por onde passa a  via Ferroviária de trem de Cusco a MachuPicchu. Começando nossa trekking desde  o posto de controle da ponte e caminharemos ao lado esquerdo do Rio Urubamba  que flui ao noroeste do Vale Sagrado. Depois de passar por alguns terraços  planos, chegaremos a Miskay (2800 ma.n.m) para logo subirmos e finalmente ver a  Cidade Inca de Patallacta (2750 m.a.n.m) , continuaremos nossa caminhada no  trajeto do vale criado pelo Rio Kusichaca, subiremos gradualmente até chegar ao  nosso primeiro acampamento de Wayllabamba (3000 m.a.n.m) onde teremos as vistas  mais espetaculares do canto do Rio Vilcanota, no lado oposto do Rio Urubamba se  pode ver o impressionante Nevado verônica (5832 m.a.n.m) ainda não e possível  calcular a diversidade de flora e fauna que se pode encontrar no local.<br>
