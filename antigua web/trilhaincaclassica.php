  <li><span dir="LTR"> </span>É possível descer de Machupicchu ao povoado de Aguas  Calientes a pé pelo caminho que Bingham subiu quando descobriu Machupicchu ou  pela própria estrada por onde circulam os microônibus que fazem o trajeto até o  povoado. É uma caminhada tranquila de aproximadamente 40 min.</li>
  <li><span dir="LTR"> </span>A equipe de apoio desempenha um trabalho árduo para  que tudo saia bem durante a caminhada. Principalmente os porteadores que fazem  o trabalho mais pesado e ganham menos. É de praxe dar gorjetas para a equipe no  último dia da trilha. Não é obrigatório, mas é de bom senso. No final da trilha  o guia passa recolhendo as gorjetas. Geralmente cada viajante oferece de 7 a 10  dólares. Isso equivale de 20 a 30 soles na moeda peruana.</li>
  <li><span dir="LTR"> </span>Por padrão está incluso o bilhete de trem de Aguas  Calientes até a estação de Ollantaytambo e depois um traslado em van da estação  de Ollantaytambo até Cusco. Isso torna o pacote da trilha inca mais barato ao  viajante além de ser mais rápido o retorno a Cusco. Caso queira podemos incluir  o bilhete de trem direto desde a estação de Aguas Calientes até Cusco. Essa  opção é um pouco mais cara, mas evita que o viajante tenha de fazer a troca de  meio de transporte quando chega na estação de Ollantaytambo.</li>
  <li><span dir="LTR"> </span>Aguas Calientes é um povoado pequeno com restaurantes  e hotéis que fica no pé da montanha de Machupicchu. É comum acontecer de o  viajante ficar em Aguas Calientes e depois se arrepender e achar que deveria  ter ido a Cusco na volta da trilha inca. Também é comum o viajante não ficar e  quando o trem esta saindo ele achar que deveria ter ficado lá. Geralmente quem  fica uma noite em Aguas Calientes é porque está descansando da caminhada na  trilha inca ou porque pretende visitar Machupicchu novamente. Caso o viajante  pretenda visitar Machupicchu novamente terá de comprar um novo ticket de  entrada de U$ 50.00 para entrar na cidade. As termas de água quente que dão o  nome ao povoado são um pouco quente e não recomendadas para banho.</li>
