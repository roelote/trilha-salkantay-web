    <div id="tab5" class="tab_content"><strong>CONDIÇÔES DE VIAGEM:</strong><br>
Para embarcar para o Peru é necessário carteira de  identidade (RG) original com menos de 10 anos de uso e em bom estado ou  passaporte válido (inclusive crianças). A embaixada chilena esta exigindo RG  original com menos de 05 anos de uso.<br>
OBS:&nbsp; sob nenhuma hipótese serão aceitos os  seguintes documentos:<br>
Certidão de Nascimento para menores de idade; RG  xerocado, rasgado, em mau estado de conservação; Carteira de Habilitação;  Documentos de classes tais como militares, OAB, CREA, CRM, etc.;<br>
<br>
<strong>DATAS DE SAIDA A TRILHA INCA.</strong><br>
Temos grupos para a trilha inca todos os meses do ano. Menos nos meses de  fevereiro quando a trilha inca é fechada durante o mês inteiro. Nos meses de  fevereiro você poderá visitar Machupicchu indo de trem ou percorrendo uma das  trilhas alternativas. Mas não será possível percorrer a trilha inca.<br>
<br>
<strong>DISPONIBILIDAD DE VAGAS: (</strong><strong>&nbsp;</strong><a href="http://www.trilhaincacuzco.com/trilhaincadisponibilidade.php" target="_blank"><strong>VEJA</strong><strong>&nbsp;</strong></a><strong>)</strong><br>
Há limite de visitantes na trilha inca. São permitidas somente 500 pessoas por  dia na trilha. Isso inclui turistas, carregadores, cozinheiros e guias. As  vagas são para o mundo todo. Por isso, para percorrer a Trilha Inca 4d/3n ou a  Trilha Inca 2d/1n, é fundamental fazer reservas antecipadas com meses de  antecedência. Independente da época em que você for viajar.<br>
A disponibilidade de vagas na trilha inca é a mesma para todas as agências de  viagens. Sendo assim, se não há vagas numa agência não haverá vagas em nenhuma  outra também.<br>
