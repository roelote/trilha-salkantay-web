<strong>Dia 03: TRILHA INCA</strong>
<h2>PACAYMAYU - CHAQICOCHA - WINAHUAYNA </h2>
Este dia é memorável por seu número de sítios  arqueológicos que visitaremos e receberemos maiores informações de nosso guia.  De Pacaymayu subiremos ao segundo passo o Abra de Runcurakay (3970 m.a.n.m ),  no meio da manhã visitaremos o complexo arqueológico do mesmo nome, este sitio  está situado a 3800 m.a.n.m, consiste em uma estrutura oval pequena que está  constituída como uma atalaia (torre de vigia). Depois de passar pelo Passo  descemos até Yanacocha (Lagoa Negra) e entraremos no Bosque nublado e  finalmente para chegar a Sayacmarca (3624 m.a.n.m). Este é um complexo lindo  feito de uma construção semi-circular com diversos níveis, de ruas estreitas,  de fontes litúrgicas, pátios e canais de irrigação. Continuaremos por uma  subida fácil, chegaremos ao terceiro passo o Abra Phuyupamarca (3700 m.a.n.m).  Ao longo da nossa caminhada podemos apreciar a magnitude da arte antiga dos  incas e ver as rochas que chegaram ao barranco em ordem perfeita. Seguiremos  nossa caminhada para logo passar por um túnel Inca e chegar ao passo já  mencionado, este é um dos complexos arqueológicos, mas completos e conservados  e se encontra no ponto mais alto da montanha. Phuyupamarca significa &quot; A  nuvem sobre a Cidade&quot; de onde poderemos observar um sofisticado complexo e  composto das fontes de águas com uma fundação sólida e com vistas  impressionantes do Rio Urubamba ao longo dele que continuaremos subindo até  Wiñayhuayna (2700 m.a.n.m). Neste sitio encontraremos uma casa de campo com um  restaurante, habitações com banheiro e chuveiro quentes. A cinco minutos do  lugar de acampamento no complexo arqueológico de Wiñayhuayna do qual è composto  de um centro agrícola com numerosos terraços religiosos e urbanos.&nbsp;<br>
