<br>
A <strong>Trilha Inca</strong> é um trecho de uma antiga  estrada inca pavimentada com pedras que atravessam as montanhas acima do rio  Urubamba e chega até a cidade de Machupicchu no Peru.<br>
<br>
Entretanto as estradas incas não se restringiram  a este trajeto. Elas ligavam Cusco em todas as direções da América do Sul  formando uma rede de comunicação de mais de 30.000 km,de <strong>Trilha Inca</strong>.<br>
<br>
Esta rede de estradas de <strong>Trilha Inca</strong> se estendia do centro do Equador até a região central  do Chile, ao sul, e da costa do oceano Pacífico até as encostas orientais dos  Andes.<br>
<br>
A <strong>Trilha  Inca</strong> é o trekking mais famoso da América do Sul e é classificado por muitos  como uma das 5 melhores trilhas do mundo.  Uma experiência única e quem o fez tem a certeza de que o esforço vale a pena!<br>
<br>
A <strong>Trilha  Inca</strong> em 45 km consegue combinar a bela paisagem das montanhas, exuberante  floresta e da selva sub-tropical. E claro, uma mistura impressionante de  construções, ruínas e túneis Incas.&nbsp;<br>
O destino final da <strong>Trilha Inca</strong> é Machu Picchu, a misteriosa &quot;cidade perdida dos  Incas&quot;.&nbsp;<br>
