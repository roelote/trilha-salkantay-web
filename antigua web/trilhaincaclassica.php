<strong>Dia 02: TRILHA INCA</strong>
<h2>HUAYLLABAMBA - WARMIWANUSCA - PACAYMAYU </h2>
Depois de um bom café da manhã, estaremos dispostos a  continuar a nossa caminhada (considerada a mais forte da viagem), consiste em  uma subida estreita e próxima a precipícios de (9 km). Ao longo desta subida a  paisagem vai se modificando de Serras Puna (área seca e alta com pouca  vegetação). Este será o momento que chegaremos ao primeiro passo da montanha  conhecida como Abra de Warmihuañuska (Passo da mulher morta) podemos observar  Llamas e Alpacas domésticas que pastam pelo Ichu ( uma das poucas plantas que  crescem nesta altitude). Também cruzaremos por uma área de bosque onde habitam  diversas classes de animais como o pardais , passarinhos e o ursos Andino.  Sugerimos levar balas, chocolates e folhas de coca, e assim mantenham o nível  alto de açúcar e controlar seu nível de Glicerina na altitude. Imediatamente  depois do passo descemos ao Vale de Pacaymayu (3600msnm) onde acamparemos  depois de 6 horas de caminhada.<br>
<div class="image"><img src="images/tours/huayllabamba1.jpg" alt="WARMIWANUSCA"><img src="images/tours/warmiwanusca.jpg" alt="TRILHA INCA"></div>
