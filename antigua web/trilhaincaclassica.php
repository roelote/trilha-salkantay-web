<strong>Dia 04: TRILHA INCA</strong>
<h2> WINAYHUAYNA - MACHU PICCHU - CUSCO </h2>
Este dia nos levantaremos às 4h00 da manhã para deixar  Wiñayhuayna na hora indicada, subiremos até o Inti Punku (porta do sol ou  nascer do Sol). O passeio nos tomará uma hora aproximadamente, depois vamos  caminhar sobre as pedras planas que se encontram na borda da montanha onde  podemos observar a saída do sol sobre a cidade de MachuPicchu. De Intipunku  desceremos até Machpichu, chegando em 40 min. Chegando em MachuPicchu teremos  uma visita guiada de aproximadamente 2 horas. Depois terão tempo livre para  caminhar aos arredores da cidadela ou subir a montanha de HuaynaPicchu de onde  poderá ver espetaculares vistas de vales e montanhas que rodeiam a cidadela de  Machupicchu ou também visitar o Templo da Lua e a famosa Ponte Inca. A tarde  desceremos ao povoado de Águas Calientes onde vocês poderão visitar os banhos  termais para relaxar depois de 4 dias de caminhada. Aqui lhe entregaremos os  Tickets de Trem de retorno ao povoado de Ollantaytambo, logo nosso ônibus lhe  estará esperando para transportar a Cidade de Cusco, chegando por volta das  19h00 ou 21h30.<br>
